import { NextResponse } from "next/server"
import { prisma } from "@/lib/prisma"
import bcrypt from "bcryptjs"
import { z } from "zod"

const registerSchema = z.object({
  name: z.string().min(1, "Name is required"),
  email: z.string().email("Invalid email address"),
  password: z.string().min(6, "Password must be at least 6 characters"),
  role: z.enum(["ADMIN", "USER"]).optional(),
})

export async function POST(request: Request) {
  try {
    const body = await request.json()
    const validatedData = registerSchema.parse(body)

    // Check if user already exists
    const existingUser = await prisma.user.findUnique({
      where: { email: validatedData.email },
    })

    if (existingUser) {
      return NextResponse.json(
        { error: "User with this email already exists" },
        { status: 400 }
      )
    }

    // Determine permission: if an admin already exists, only an ADMIN session can create users
    const adminCount = await prisma.user.count({ where: { role: "ADMIN" } })
    if (adminCount > 0) {
      // Require admin session
      // Import auth lazily to avoid circular
      const { auth } = await import("@/lib/auth")
      const session = await auth()
      if (!session || session.user?.role !== "ADMIN") {
        return NextResponse.json({ error: "Only admin can create new users" }, { status: 403 })
      }
    }

    // Determine role
    let roleToSet: "ADMIN" | "USER" = "USER"
    if (validatedData.role === "ADMIN" && adminCount === 0) {
      roleToSet = "ADMIN" // bootstrap first admin only
    }

    // Hash password
    const hashedPassword = await bcrypt.hash(validatedData.password, 10)

    // Create user
    const user = await prisma.user.create({
      data: {
        name: validatedData.name,
        email: validatedData.email,
        password: hashedPassword,
        role: roleToSet,
      },
      select: {
        id: true,
        name: true,
        email: true,
        role: true,
      },
    })

    return NextResponse.json(
      { message: "User created successfully", user },
      { status: 201 }
    )
  } catch (error) {
    if (error instanceof z.ZodError) {
      return NextResponse.json(
        { error: error.errors[0].message },
        { status: 400 }
      )
    }

    console.error("Registration error:", error)
    return NextResponse.json(
      { error: "Internal server error" },
      { status: 500 }
    )
  }
}


